package string_Study;

public class Sample {
	public static void main(String[] args)
	{
	// object creation of String can be done in 2 ways:
	// 1. without using new keyword --> String s1="abc";
	// 2. using new keyword --> String s2=new String("xyz");

	//1. without using new keyword
	String city="Pune";
	System.out.println("Name of city is "+city);
	
	//2. using new keyword
	String state= new String("Maharashtra");
	System.out.println("Name of state is "+state);
	}
}