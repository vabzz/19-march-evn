package loops;

public class While_Loop {

	public static void main(String[] args) {
		
		int i=2;
		while(i<=20)
		{
			System.out.println(i);
			i=i+2;
		}

		
		int j=10;
		while(j<=100)
		{
			System.out.println(j);
			j=j+10;
		}
		
		String Name="Velocity";
		int k=1;
		while(k<=10)
		{
			System.out.println(Name);
			k++;
		}
		char z='A';
		while(z<='Z')
		{
			System.out.println(z);
			z++;
		}
	}

}
